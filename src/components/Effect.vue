<h1>目標を達成しよう！</h1>
  <p>ここでの目標は、「食事から健康的な生活を送ること」である。
    <br>そのために必要な栄養素を知り、日々の食事に取り入れ、より良い食生活を送って欲しいと思う。
    <br>ここでは、自分に必要な栄養素を科学的に得てほしいと思う。しかし、それに納得ができても、勤しむ日々の食生活でいちいち科学的に献立を考えるのは極めて難しい。
    <br>だからこそ私たちはマーケティング的に物事を判断するのである。その方が直感的で言語化しやすく、自分に言い聞かせられ、より行動にうつしやすくなるからである。
    <br>そして行動する上で直感に頼るのは素敵であり、大事にしなければならない。私はその「直感さ」を大切にアプリを作成した。その工夫として、「投げかけの質問」「選びやすさ」「グラフによる可視化」などを実装した。
    <br>その「直感さ」持ってして、科学的で、より良い食生活を送ってほしいと思う。
    <br>そのような思いがあり、時間の限られた中でより直感的に献立を立てたい方々へ向けたアプリを提供すべく、さまざまな機能を実装した。
  </p>